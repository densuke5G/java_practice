package practicepackage;

public class Whilebun {
	public static void main(String[] args) {
		int i = 0;
        while (i <= 9){
            System.out.println("ハローラーニング");
            i = i + 1;
        }
        i = 0;
        while (i <= 15) {
            System.out.println(i);
            i = i + 1;
        }
        i = 2;
        while (i <= 10) {
            System.out.println(i);
            i = i + 2;
        }
	}

}
